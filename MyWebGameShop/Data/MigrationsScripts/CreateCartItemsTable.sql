CREATE TABLE CartItems (
    Id SERIAL PRIMARY KEY,
    Quantity INTEGER NOT NULL,
    GameId INTEGER NOT NULL,
    UserId INTEGER NOT NULL,
    OrderId UUID NULL,
    CONSTRAINT FK_CartItems_Games FOREIGN KEY (GameId) REFERENCES Games(Id) ON DELETE CASCADE,
    CONSTRAINT FK_CartItems_Users FOREIGN KEY (UserId) REFERENCES Users(Id) ON DELETE CASCADE,
    CONSTRAINT FK_CartItems_Orders FOREIGN KEY (OrderId) REFERENCES Orders(Id) ON DELETE SET NULL
);